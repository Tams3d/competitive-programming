#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string s1, s2;
    cin >> s1 >> s2;

    for (char& c : s1)
        c = tolower(c);
    for (char& c : s2)
        c = tolower(c);

    cout << (s1 > s2) - (s1 < s2);

    return 0;
}
